<!DOCTYPE html>
<html>
    <head>
        <script>
        
            var client = new XMLHttpRequest();
            client.open('GET', './head.html');
            client.onreadystatechange = function() {
                document.getElementsByTagName('head').innerHTML = client.responseText;
            }
            client.send();
            
        </script>
    </head>
    <body>
        <h1>Hallo Welt!</h1>
    </body>
</html>